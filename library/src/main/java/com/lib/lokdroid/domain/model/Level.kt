package com.lib.lokdroid.domain.model

/**
 * Represents the logging levels used to categorize the importance and nature of log messages.
 *
 * This enum class defines various levels of logging that can be used to control logging output
 * and to distinguish between kinds of log events, from detailed messages intended for
 * debugging to high-severity error messages.
 */

enum class Level { Verbose, Debug, Info, Warn, Error }